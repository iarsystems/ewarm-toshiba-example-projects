#define LLC_LLCNT_HOLD_EN                   0x4004024CL /* 0x40040240 + 0x0C */
#define LLC_BC_LATCH_MA                     0x40040260L /* 0x40040240 + 0x20 */

static RET_API SPI_API_SetCallbackWakeup( SYS_API_CB_FUNC CB_Func)
{
    return ((RET_API  (*)( SYS_API_CB_FUNC CB_Func)) 0x000004e5)(CB_Func) ;
}
static void CallbackWakeup(void){
  #if PATCH_SWITCH_WHITELIST_NONCONN
    uint16_t* ptr =   (uint16_t*)0x0080d618;
    uint16_t* cause = (uint16_t*)0x00804886;
  #endif

  #if PATCH_SWITCH_ANYKEY_CAUSE_REG
    uint32_t current_time;
    uint32_t new_time = 0;

    REG_WR32( LLC_LLCNT_HOLD_EN, 0 );
    __NOP();__NOP();__NOP();__NOP();
    current_time = REG_RD32(LLC_BC_LATCH_MA);
  #endif

  #if PATCH_SWITCH_WHITELIST_NONCONN
    if( *cause & ((1<<2)|(1<<5)) ){
        *ptr = 1;
    }
  #endif

  #if PATCH_SWITCH_ANYKEY_CAUSE_REG
    /* 75us delay */
    while( new_time <= 75 ){
        REG_WR32( LLC_LLCNT_HOLD_EN, 0 );
        __NOP();__NOP();__NOP();__NOP();
        new_time = REG_RD32(LLC_BC_LATCH_MA);
        if(new_time > current_time){
            new_time = new_time - current_time;
        } else {
            new_time = (625 - current_time) + new_time;
        }
    }
  #endif
    return;
}
static void SET_SWITCH_ANYKEY_CAUSE_REG_OR_WHITELIST_NONCONN( void ){
    SPI_API_SetCallbackWakeup( CallbackWakeup );
}
